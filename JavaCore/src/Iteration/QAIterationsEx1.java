package Iteration;

public class QAIterationsEx1 {

	    public static void main(String[] args) {
	        for(int A = 0; A > 100; A++) {
	            if(A == 1) {
	                continue;
	            }
	            if(A > 200) {
	                break;
	            }
	            System.out.println(A); 
	            } 
	        } 
	    }
